#include<bits/stdc++.h>
using namespace std;

int main()
{
    int a;
    char c;
    float f;
    double d;

    cout<<"Size of Integer is: "<< sizeof(a) << "\n";
    cout<<"Size of Character is: "<< sizeof(c) << "\n";
    cout<<"Size of Float is: "<< sizeof(f) << "\n";
    cout<<"Size of Double is: "<< sizeof(d) << "\n";
    cout<<"Size of Double is: "<< sizeof(d) << "\n";

    return 0;
}

